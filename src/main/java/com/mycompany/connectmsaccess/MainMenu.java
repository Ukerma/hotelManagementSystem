    private void jLabel28MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel28MouseClicked
        String roomNo = textRoomNumber.getText();
        try {
            PreparedStatement pst = conn.prepareStatement("SELECT * FROM guest WHERE roomNo='" + roomNo + "' and checkOut is NULL");
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                id = rs.getInt("ID");
                textGuestName.setText(rs.getString("name"));
                textGuestCheckIn.setText(rs.getString("checkIn"));
                textGuestNo.setText(rs.getString("phoneNo"));
                textPPP.setText(rs.getString("pricePerDay"));

                SimpleDateFormat myFormat = new SimpleDateFormat("MM/dd/yyyy");
                Calendar cal = Calendar.getInstance();
                textCheckOut.setText(myFormat.format(cal.getTime()));
                String dateBeforeString = rs.getString("checkIn");
                java.util.Date dateBefore = myFormat.parse(dateBeforeString);
                String dateAfterString = myFormat.format(cal.getTime());
                java.util.Date dateAfter = myFormat.parse(dateAfterString);
                long difference = dateAfter.getTime() - dateBefore.getTime();
                int noOfDayStay = (int) (difference / (1000 * 60 * 60 * 24));
                if (noOfDayStay == 0) {
                    noOfDayStay = 1;
                }
                textNDS.setText(String.valueOf(noOfDayStay));
                float price = Float.parseFloat(textPPP.getText());

                textAmount.setText(String.valueOf(noOfDayStay * price));
                textGuestMail.setText(rs.getString("eMail"));
                roomType = rs.getString("roomType");
                bed = rs.getString("bed");
            } else {
                JOptionPane.showMessageDialog(null, "Room Number is Booked or doesn't Existé");

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jLabel28MouseClicked


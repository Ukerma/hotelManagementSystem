    private void fillTable1(Connection conn) throws SQLException {
        // Bağlantıyı kullanarak sorguyu çalıştır ve ilk tabloyu doldur
        PreparedStatement pst1 = conn.prepareStatement("SELECT * FROM rooms");
        ResultSet rs1 = pst1.executeQuery();
        DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();

        // Modeli temizle
        model1.setRowCount(0);

        // Sonuç kümesindeki her bir satırı işle
        while (rs1.next()) {
            String roomNo = rs1.getString("roomNo");
            String roomType = rs1.getString("roomType");
            String bed = rs1.getString("bed");
            String price = rs1.getString("price");
            String status = rs1.getString("status");

            // Yeni satırı modelin sonuna ekle
            model1.addRow(new Object[]{roomNo, roomType, bed, price, status});
        }

        // Tabloyu yeniden çiz
        jTable1.revalidate();

        // Kullanılan kaynakları kapat
        rs1.close();
        pst1.close();
    }


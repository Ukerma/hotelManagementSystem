    private void jLabel41MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel41MouseClicked
        String checkOutDate = textPrintBillDate.getText();
        try {
            Connection conn = ConnectMsAccess.conn();

            PreparedStatement pst3 = conn.prepareStatement("SELECT * FROM guest where checkOut='" + checkOutDate + "'");
            ResultSet rs3 = pst3.executeQuery();
            DefaultTableModel model2 = (DefaultTableModel) jTable3.getModel();
            model2.setRowCount(0);

            // Sonuç kümesindeki her bir satırı işle
            while (rs3.next()) {

                // Yeni satırı modelin sonuna ekle
                model2.addRow(new Object[]{rs3.getString("ID"), rs3.getString("name"), rs3.getString("surname"), rs3.getString("idNo"), rs3.getString("phoneNo"), rs3.getString("nationality"), rs3.getString("eMail"), rs3.getString("address"), rs3.getString("gender"), rs3.getString("checkIn"), rs3.getString("roomNo"), rs3.getString("bed"), rs3.getString("roomType"), rs3.getString("pricePerDay"), rs3.getString("numberOfDayStay"), rs3.getString("totalAmount"), rs3.getString("checkOut")});
            }

            // Tabloyu yeniden çiz
            jTable2.revalidate();

            // Kullanılan kaynakları kapat
            rs3.close();
            pst3.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jLabel41MouseClicked

